public class IfElse {
    public static void main(String[] args) {
        // if else statements in java 
        int grade = 65;

        if (grade >= 80) {
            System.out.println("You got an A");
        } else if (grade >= 70) {
            System.out.println("You got a B");
        } else if (grade >= 60 ){
            System.out.println("You got a C");
        }else {
            System.out.println("You Didn't passed");
        }
    }
}
