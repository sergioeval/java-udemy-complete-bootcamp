public class IntroToArrays {
  public static void main(String[] args) {
    // Introduction to arrays  - the elemnts inside need to be of the same type
    String[] kingdoms = {"Mercia", "Wessex", "Northumbria", "E A"};
    System.out.println(kingdoms); // this will only print the reference to the array

    // arrays are index on 0
    System.out.println(kingdoms[0]);

    //len of an array 
    System.out.println(kingdoms.length);
  }
}
